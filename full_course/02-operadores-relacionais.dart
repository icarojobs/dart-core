main() {
  var a = 10, b = 12;

  print('Valor de A=$a, Valor de B=$b');
  print('--------------------------------------');
  print('A é igual a B: ${a == b}');
  print('A é menor que B: ${a < b}');
  print('A é maior que B: ${a > b}');
  print('A é menor ou igual que B: ${a <= b}');
  print('A é maior ou igual que B: ${a >= b}');
  print('A é diferente de B: ${a != b}');
}